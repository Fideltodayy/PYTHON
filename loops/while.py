#The while loop
#The while loop is used to repeat a section of code an unknown number of times until a specific condition is met. For example, say we want to know how many times a given number can be divided by 2 before it is less than or equal to 1. We don't know how many times this will be, so we can't use a for loop. Instead, we can use a while loop.
i = 0
while i <10:
    print(i)
    i +=1
else:
    print("Done with all the work")